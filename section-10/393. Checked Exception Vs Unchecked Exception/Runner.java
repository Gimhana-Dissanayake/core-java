// There are two types of predefined Exceptions.
// 1.Checked Exceptions
// 2.UnChecked Exceptions

// Q. What is the difference between CheckedException and UncheckedException

// Ans. Checked Exception is an Exception identified/ Recognized / predicted at
// compilation time.

// NOTE: In java, all exceptions will occure at runtime only, but, checked
// exceptions are identified by compiler by monitoring our code and compiler
// will give a notice to use that this paticular exception will come at runtime
// if we execute this program.

// UncheckedException is an exception recognized or identified at runtime.

// EX - > RuntimeException and its Sub classes, Error and its sub classes are
// the examples for uncheckedExceptions. All the remaining exception are the
// example for checked exception