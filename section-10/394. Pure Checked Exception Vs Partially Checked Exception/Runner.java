// In Java, there are two types of checked exceptions.
// 1. Pure checked exception
// 2. Partially Checked Exception

// Q. What is the difference between Pure checked Exception and Partially
// Checked Exception

// Ans. If any CheckedExceptions has only checked exceptions as sub classes then
// that checked exception is called as pure checked exception.

// EX:- IOException

// If any checked Exception has atleaset one sub class as unchecked exception
// then that checked exception is called as partially checked exception.

// EX:- Exception, Throwable