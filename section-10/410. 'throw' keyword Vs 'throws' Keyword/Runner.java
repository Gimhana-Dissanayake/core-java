// Ans:
// 1. 'throw' keyword can be used to raise the exceptions intentionally as per
// the applications requriement.

// 'throws' keyword is able to bypass the exception from present method to
// caller method.

// 2. 'throw' keywrod will be used in method implemention or in method body.

// 'throws' keyword will be used in method declaration or in method prototype.

// 3. 'throw' keyword is able to allow only one Exception class name.

// 'throws' keyword is able to allow any no of exception classes.

