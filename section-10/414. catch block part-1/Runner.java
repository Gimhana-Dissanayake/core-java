// catch block:
// the main intention of catch block is to catch the generated exception from
// try block and display the respective exception details to the user.

// In try-catch-finally, catch block will be executed when we have an exception
// in try block.

// Catch block is able to take an exception class name as parameter, it should
// be either same as the generated exception in try block or it should be a
// super class to the generated exception in try block.

// In catch block we are able to display exeption details in the following three
// ways.

// 1. By using printStackTrace() method.
// 2. By using system.out.println(refVar) or toString()
// 3. By using getMessage() method