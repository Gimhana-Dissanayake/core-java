// IOStreams:
// In general, in every programming language we have to perform input and output
// operations, to perform these input and output operations we have to use some
// predefined library provided by the respective programming language.

// In C:
// printf() for output operations
// scanf() for input operations

// Similarily, in java applications, to perform input and output operations we
// have to use "Streams".

// Stream is a channel or medium, it allows data in continous flow from input
// devices[Keyboard, Mouse, Network...] to Java applications and from Java
// applications to output devices[Monitor, Printer, Network...]