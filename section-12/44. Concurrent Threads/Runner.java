// Concurrent Threads:

// In multi threading, we may create more than one thread and we may execute
// more than one thread at a time.

// If we execute more than one thread on single data item or on single program
// then that threads are called as "Concurrent Threads" and that process is
// called as "Thread Concurrency"

// In the case of Threads concurrency there may be chances are available to get
// data inconsistency, it will provide wrong results in java applications.

// To overcome the above problems we need "threadsafe" resource.

// If any resource allows more than one thread at a time without providing data
// inconsistency then that resource is called as "Threadsafe Resource"