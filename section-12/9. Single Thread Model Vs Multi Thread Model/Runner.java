// There are two thread models to execute applications.
// 1. Single Thread Modal / Single Tasking
// 2. Multi Thread Modal / Multi Tasking

// 1. Single Thread Modal / Single Tasking:
// It allows only one thread to execute the complete applications.
// It follows sequential execution of the applications.
// It will increase application execution time.
// it will reduce application performance

// 2. Multi Thread Modal / Multi Tasking
// It allows more than one thread to execute the complete application.
// It follows parallel execution.
// It will readuce application execution time.
// It will increase application performance.

// NOTE: Almost all the technologies using Multi Thread Modal.

// Java is Thread Based programming language
