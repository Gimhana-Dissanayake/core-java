// Arrays:
// ----------------

// Array:
// Array is an object, it is able to store more than one element of the same
// data type as per indexing, where index values will start from 0 to size-1

// In java applications, we are able to utilize arrays in the following two ways

// 1. Declare and Initialize
// 2. Declare then Initialize

// 1. Declare and Initialize.
// -------------------------------
// In this approach, we will decalre the array and we will provide elements to
// the array[Initialization] in single instruction.

// Ex: int[] a = {10,20,30}

// 2. Declare then Initialize
// -------------------------------
// In this approach, we will declare the array in one instruction and we will
// initialize the array in the next instructions.
// Ex: int[] a = new int[3];
// a[0] = 12;
// a[1] = 10;
// a[2] = 103;
