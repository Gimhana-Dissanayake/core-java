// Vector:
// Vector was introduces in JDK1.0 version
// Vector is a Legacy Collection
// Vector is a direct implementation class to List interface, List interface is child interface, so, vector has to provide implementation for all the methods of List interface and Collection interface.

// Vector is index based, it allows all the elements as per indexing.
// Vector allows duplicate elements.
// Vector follows insertion order
// Vector does not follow sorting order.
// Vector allows heterogenous elements.
// Vector allows more than one null elements.

// Vector interface data structer is "Resizable-Array".
// Vector initial capacity is 10 elements.
// Vector is able to follow the incremental capacity with the following formula

// NewCapacity = 2 * CurrentCapacity
// Vector is suggestible when we want to perform frequent retrieval operations.

// Vector is a Synchronized Collection.
// Vector has almost all the methods as synchronized methods.
// Vecotr is able to allow only one thread at at time to access data.
// Vector follows sequenctial execution of the threads.
// Vector increases application execution time
// Vector will reduces application performance.
// Vector gives guarantee for Data Consistency.
// Vector is Threadsafe Resource.

