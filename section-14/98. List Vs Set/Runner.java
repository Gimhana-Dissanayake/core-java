// Q. What are difference between List and Set?

// Ans:
// -----------
// 1. List is index based, it allows all the elements as per indexing, where
// index values will start from 0 to size - 1. Set is not index based, it able
// to store all the elements on the basis of elements hashcode values.

// 2. List allows duplicated elements. Set does not allow duplicate elements

// 3. List fallows insertion order. All the implementations of List follows
// insertion order. Set does not follow insertion order, but, LinkedHashSet
// follows insertion order.

// 4. List does not follow sorting order, All the implementations of List does
// not follow sorting order.

// Set does not follow Sorting order, but, SortedSet, NavigableSet and TreeSet
// are fallowing Sorting Order.

// 5. List and its implementaions allows Heterogeneous elements.
// Set allows Heterogeneous elements, but, SortedSet, NavigableSet and TreeSet
// are not allowing Heterogeneous elements.

// 6. List allows any number of null values.
// Set allows only one null value, because, Sets are not allowing duplicate
// elements
