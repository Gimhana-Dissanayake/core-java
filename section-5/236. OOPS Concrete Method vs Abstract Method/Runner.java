// Q. What is the difference between concrete method and abstract method

// Ans. Concrete method is a normal java method, it will have both method declaration and method implementation[method body]
// Abstract method is a java method, it will have only Method declaration without method implementation[Method body]
// Concrete method is called as "Completed method" where as abstract method is called as "Incompleted method"

// To declare concrete methods we will not use any special keywords. To declare abstract methods we must use 'abstract' keyword

// Concreate methods are possible in classes and abstract classes. Abstract methods are possible in abstract classes and interfaces

// Concrete methods are providing less sharability. Abstract methods are providing more sharability.
